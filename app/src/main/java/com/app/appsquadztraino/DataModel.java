package com.app.appsquadztraino;

public class DataModel {

    private String mini, mainTittle, desc;
    int price;

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getMini() {
        return mini;
    }

    public void setMini(String mini) {
        this.mini = mini;
    }

    public String getMainTittle() {
        return mainTittle;
    }

    public void setMainTittle(String mainTittle) {
        this.mainTittle = mainTittle;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
}
